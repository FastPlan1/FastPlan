router.post("/location", isChauffeur, async (req, res) => {
  console.log("üü° [EMP] POST update-location for chauffeur", req.user.id);
  try {
    const { latitude, longitude } = req.body;
    if (typeof latitude !== "number" || typeof longitude !== "number") {
      console.warn("‚ö†Ô∏è  [EMP] Coordonn√©es invalides :", req.body);
      return res.status(400).json({ message: "latitude et longitude num√©riques requis." });
    }

    const user = await User.findById(req.user.id);
    if (!user) {
      console.warn("‚ö†Ô∏è  [EMP] Chauffeur non trouv√© :", req.user.id);
      return res.status(404).json({ message: "Chauffeur non trouv√©." });
    }

    user.latitude = latitude;
    user.longitude = longitude;
    user.updatedAt = new Date();
    await user.save();

    console.log("‚úÖ [EMP] Position mise √† jour :", latitude, longitude);
    res.json({ message: "Position mise √† jour." });
  } catch (err) {
    console.error("‚ùå [EMP] Erreur mise √† jour position :", err);
    res.status(500).json({ message: "Erreur serveur" });
  }
});

